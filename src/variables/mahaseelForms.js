export const getTranslation = (...args) => {
  let value = translations;
  for (let i = 0; i < args.length; i++) {
    const ele = args[i];
    value = value[ele];
    if (value === undefined) {
      return console.error(`key ${ele} is not found`);
    }
  }
  return value;
};
